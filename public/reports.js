async function generateReportPayroll() {
    var from = document.getElementById('rep-pay-from').value;
    var to = document.getElementById('rep-pay-to').value;
    var staffId = document.getElementById('rep-pay-staff').value;
    var dept = document.getElementById('rep-pay-dept').value;
    var staffSel = document.getElementById('rep-pay-staff');
    var deptSel = document.getElementById('rep-pay-dept');
    var staffLabel = staffId ? staffSel.options[staffSel.selectedIndex].textContent : 'All Staff';
    var deptLabel = dept || 'All Departments';

    // Always re-fetch fresh from the database — no client-side caching of payroll records.
    var res = await fetch('/api/payslips');
    var all = await res.json();
    var list = all.filter(p => {
        if (from && p.paydate && p.paydate < from) return false;
        if (to && p.paydate && p.paydate > to) return false;
        if (staffId && p.staffId !== staffId) return false;
        if (dept && p.department !== dept) return false;
        return true;
    });
    var out = document.getElementById('rep-pay-out');

    if(!list.length) {
        out.innerHTML = '<div class="card" style="text-align:center;color:#888;padding:2rem">No payroll records found matching this search.</div>';
        return;
    }

    var companyName = await getCompanyName();
    var rangeLabel = (from && to) ? (fmtDate(from) + ' &ndash; ' + fmtDate(to)) : from ? ('From ' + fmtDate(from)) : to ? ('Up to ' + fmtDate(to)) : 'All Time';

    var tE=list.reduce((s,p)=>s+(Number(p.totalEarn)||0),0);
    var tV=list.reduce((s,p)=>s+(Number(p.vnpf)||0),0);
    var tL=list.reduce((s,p)=>s+(Number(p.loan)||0),0);
    var tO=list.reduce((s,p)=>s+(Number(p.others)||0),0);
    var tN=list.reduce((s,p)=>s+(Number(p.net)||0),0);

    out.innerHTML = `
        <div id="payroll-report-printable">
        <div class="rpt-page">
            ${rptHeader('Payroll Report', companyName)}
            <div class="rpt-meta" style="grid-template-columns:repeat(auto-fit, minmax(140px, 1fr))">
                <div class="rpt-meta-item"><p>Date Range</p><span>${rangeLabel}</span></div>
                <div class="rpt-meta-item"><p>Staff</p><span>${staffLabel}</span></div>
                <div class="rpt-meta-item"><p>Department</p><span>${deptLabel}</span></div>
                <div class="rpt-meta-item"><p>Generated On</p><span>${rptToday()}</span></div>
                <div class="rpt-meta-item"><p>Status</p><span style="color:#10b981">Live</span></div>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">1.</span> Payroll Summary</p>
                <table class="rpt-summary-table">
                    <tr><td>Total Gross Payroll</td><td>${vuvFmt(tE)}</td></tr>
                    <tr><td>Total VNPF</td><td>${vuvFmt(tV)}</td></tr>
                    <tr><td>Total Other Deductions</td><td>${vuvFmt(tL+tO)}</td></tr>
                    <tr style="background:#f8f9fb"><td>Total Net Payroll</td><td>${vuvFmt(tN)}</td></tr>
                </table>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">2.</span> Processed Payslips (${list.length})</p>
                <table class="rpt-detail-table">
                    <thead><tr><th>Staff</th><th>Department</th><th>Pay Date</th><th style="text-align:right">Gross</th><th style="text-align:right">VNPF</th><th style="text-align:right">Net</th></tr></thead>
                    <tbody>
                        ${list.slice().sort((a,b) => new Date(b.paydate||0) - new Date(a.paydate||0)).map(p => `<tr><td>${p.staff}</td><td>${p.department||'—'}</td><td>${p.paydate?fmtDate(p.paydate):'—'}</td><td style="text-align:right">${vuvFmt(p.totalEarn)}</td><td style="text-align:right">${vuvFmt(p.vnpf)}</td><td style="text-align:right;font-weight:700">${vuvFmt(p.net)}</td></tr>`).join('')}
                    </tbody>
                </table>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">3.</span> Payroll Breakdown</p>
                <div style="position:relative;height:280px;width:100%"><canvas id="payReportChart"></canvas></div>
            </div>
            ${rptFooter()}
        </div>
        </div>
        ${reportActionBar('payroll-report-printable', 'Payroll-Report.pdf', 'Payroll Report - '+staffLabel)}
    `;

    setTimeout(() => {
        if(typeof createChart !== 'function') return;
        createChart('payReportChart', 'doughnut', {
            labels: ['Total Net', 'Total VNPF', 'Other Deductions'],
            datasets: [{
                data: [tN, tV, tL + tO],
                backgroundColor: ['#10b981', '#f59e0b', '#ef4444']
            }]
        });
    }, 100);
}


async function generateReportVNPF() {
    var m = document.getElementById('rep-vnpf-month').value;
    var y = document.getElementById('rep-vnpf-year').value;
    var [psRes, staffRes, settingsRes] = await Promise.all([fetch('/api/payslips'), fetch('/api/staff'), fetch('/api/settings')]);
    var all = await psRes.json();
    var staffList = await staffRes.json();
    var settingsList = await settingsRes.json();
    var staffById = {};
    staffList.forEach(s => { staffById[s._id] = s; });
    var companySettings = settingsList.find(s => s._id === 'company') || {};
    // Employer contribution is recalculated at the CURRENT company VNPF rate (Company Settings),
    // while the employee figure is the amount already stored on the payslip at save time — these
    // can differ if the rate has changed since a payslip was processed.
    var vP = companySettings.vnpfPercent ? parseFloat(companySettings.vnpfPercent) : 6;
    var companyName = companySettings.name || 'WokManeja';

    var list = all.filter(p => (!m || p.month === m) && (!y || String(p.year) === y));
    var out = document.getElementById('rep-vnpf-out');

    if(!list.length) {
        out.innerHTML = '<div class="card" style="text-align:center;color:#888;padding:2rem">No VNPF records for this period.</div>';
        return;
    }

    var periodLabel = m && y ? m + ' ' + y : y ? 'Year ' + y : 'All Periods';
    var today = new Date().toLocaleDateString('en-GB', { day: '2-digit', month: 'long', year: 'numeric' });

    var tGross=0, tEmp=0, tEmployer=0;
    var rows = '';
    list.forEach(p => {
        var s = staffById[p.staffId] || {};
        var name = p.staff || s.name || 'Unknown';
        var vnpfNo = s.vnpfno || 'N/A';
        var gross = parseFloat(p.totalEarn) || 0;
        var emp = parseFloat(p.vnpf) || 0;
        var empr = Math.round(gross * (vP/100));
        tGross += gross; tEmp += emp; tEmployer += empr;
        rows += '<tr>'+
            '<td>'+name+'</td>'+
            '<td style="color:#666">'+(p.empid||'—')+'</td>'+
            '<td style="font-weight:600;color:#185fa5">'+vnpfNo+'</td>'+
            '<td style="text-align:right">'+vuvFmt(gross)+'</td>'+
            '<td style="text-align:right;color:#a32d2d">'+vuvFmt(emp)+'</td>'+
            '<td style="text-align:right;color:#a32d2d">'+vuvFmt(empr)+'</td>'+
            '<td style="text-align:right;font-weight:700;color:#0a0a0a">'+vuvFmt(emp+empr)+'</td>'+
        '</tr>';
    });
    var tTotal = tEmp + tEmployer;

    var html = '<div id="vnpf-report-printable">'+
        '<div class="rpt-page">'+
            rptHeader('VNPF Remittance', companyName)+
            rptMeta(periodLabel, today, 'Finalized')+
            '<div class="rpt-section">'+
                '<p class="rpt-section-title"><span class="rpt-section-num">1.</span> VNPF Remittance Summary</p>'+
                '<table class="rpt-summary-table">'+
                    '<tr><td>Total Gross Pay</td><td>'+vuvFmt(tGross)+'</td></tr>'+
                    '<tr><td>Employee Deduction</td><td>'+vuvFmt(tEmp)+'</td></tr>'+
                    '<tr><td>Employer Contribution</td><td>'+vuvFmt(tEmployer)+'</td></tr>'+
                    '<tr style="background:#f8f9fb"><td>Total VNPF Payable</td><td>'+vuvFmt(tTotal)+'</td></tr>'+
                '</table>'+
            '</div>'+
            '<div class="rpt-section">'+
                '<p class="rpt-section-title"><span class="rpt-section-num">2.</span> VNPF Breakdown</p>'+
                '<table class="rpt-detail-table">'+
                    '<thead><tr><th>Staff</th><th>ID</th><th>VNPF Number</th><th style="text-align:right">Gross Pay</th><th style="text-align:right">Employee '+vP+'%</th><th style="text-align:right">Employer VNPF</th><th style="text-align:right">Total VNPF</th></tr></thead>'+
                    '<tbody>'+rows+'</tbody>'+
                '</table>'+
            '</div>'+
            rptFooter()+
        '</div>'+
    '</div>';
    html += reportActionBar('vnpf-report-printable', 'VNPF-Remittance-Report.pdf', 'VNPF Remittance Report - '+periodLabel);
    out.innerHTML = html;
}

// 6. Executive Summary
async function renderReportExecutive() {
    var el = document.getElementById('section-report-executive');
    el.innerHTML = '<div style="text-align:center;padding:2rem"><i class="ti ti-loader-2" style="animation:spin 1s linear infinite;font-size:2rem"></i><p>Aggregating Executive Summary...</p></div>';

    // Aggregate across modules
    var [staffRes, payslipsRes, entriesRes, hrRes] = await Promise.all([
        fetch('/api/staff', { headers: { 'Authorization': 'Bearer ' + sessionStorage.getItem('api_token') }}),
        fetch('/api/payslips', { headers: { 'Authorization': 'Bearer ' + sessionStorage.getItem('api_token') }}),
        fetch('/api/fin/journals', { headers: { 'Authorization': 'Bearer ' + sessionStorage.getItem('api_token') }}),
        fetch('/api/hr_requests', { headers: { 'Authorization': 'Bearer ' + sessionStorage.getItem('api_token') }})
    ]);

    var staff = await staffRes.json();
    var payslips = await payslipsRes.json();
    var entries = await entriesRes.json();
    var hrReqs = await hrRes.json();
    var companyName = await getCompanyName();

    var thisYear = new Date().getFullYear();
    var totalStaff = staff.length;
    var activeStaff = staff.filter(s => s.status !== 'Inactive').length;
    var ytdPayslips = payslips.filter(p => String(p.year) === String(thisYear));
    var ytdPayroll = ytdPayslips.reduce((s,p)=>s+(Number(p.totalEarn)||0),0);
    var ytdVNPF = ytdPayslips.reduce((s,p)=>s+(Number(p.vnpf)||0),0);
    var pendingHR = hrReqs.filter(r=>r.status==='Pending').length;

    // Account balances (mirrors Finance Dashboard logic: code/name/type aware)
    var balances = {};
    entries.forEach(e => {
        if(!e.lines) return;
        e.lines.forEach(l => {
            if(!balances[l.account_code]) balances[l.account_code] = { name: l.account_name, type: l.account_type, val: 0 };
            balances[l.account_code].val += (parseFloat(l.debit)||0) - (parseFloat(l.credit)||0);
        });
    });
    var rev = 0, expenses = 0, cash = 0;
    Object.keys(balances).forEach(code => {
        var a = balances[code];
        if(a.type === 'Revenue') rev -= a.val;
        if(a.type === 'Expense') expenses += a.val;
        if(code === '1000' || (a.name||'').toLowerCase().includes('cash') || (a.name||'').toLowerCase().includes('bank')) cash += a.val;
    });
    var netProfit = rev - expenses;

    // 6-month Revenue vs Expense trend + expense breakdown (mirrors Finance Dashboard)
    var monthlyData = {};
    var today = new Date();
    for (var i=5; i>=0; i--) {
        var d = new Date(today.getFullYear(), today.getMonth()-i, 1);
        monthlyData[d.toLocaleString('default',{month:'short',year:'2-digit'})] = { income:0, expense:0, sortKey:d.getTime() };
    }
    var expenseBreakdown = {};
    entries.forEach(e => {
        if(!e.lines || !e.date) return;
        var label = new Date(e.date).toLocaleString('default',{month:'short',year:'2-digit'});
        e.lines.forEach(l => {
            var amt = (parseFloat(l.debit)||0) - (parseFloat(l.credit)||0);
            if(monthlyData[label]) {
                if(l.account_type==='Revenue') monthlyData[label].income += -amt;
                if(l.account_type==='Expense') monthlyData[label].expense += amt;
            }
            if(l.account_type==='Expense' && amt>0) expenseBreakdown[l.account_name] = (expenseBreakdown[l.account_name]||0)+amt;
        });
    });
    var sortedMonths = Object.keys(monthlyData).sort((a,b)=>monthlyData[a].sortKey-monthlyData[b].sortKey);
    var trendLabels = sortedMonths, trendIncome = sortedMonths.map(m=>monthlyData[m].income), trendExpense = sortedMonths.map(m=>monthlyData[m].expense);
    var expSorted = Object.keys(expenseBreakdown).sort((a,b)=>expenseBreakdown[b]-expenseBreakdown[a]).slice(0,6);

    // Staff by department
    var depts = {};
    staff.forEach(s => { var d = s.department||'Unknown'; depts[d]=(depts[d]||0)+1; });

    // HR requests by status
    var hrApproved = hrReqs.filter(r=>r.status==='Approved').length;
    var hrRejected = hrReqs.filter(r=>r.status==='Rejected').length;

    // Additional derived metrics for the Narrative Overview
    var profitMargin = rev > 0 ? (netProfit/rev*100) : 0;
    var avgPayrollPerEmployee = activeStaff > 0 ? (ytdPayroll/activeStaff) : 0;
    var avgMonthlyExpense = trendExpense.length ? (trendExpense.reduce((a,b)=>a+b,0)/trendExpense.length) : 0;
    var cashRunwayMonths = avgMonthlyExpense > 0 ? (cash/avgMonthlyExpense) : null;
    var topExpenseCat = expSorted.length ? expSorted[0] : null;
    var topExpenseAmt = topExpenseCat ? expenseBreakdown[topExpenseCat] : 0;
    var topExpenseShare = (topExpenseCat && expenses > 0) ? (topExpenseAmt/expenses*100) : 0;
    var topDept = null, topDeptCount = 0;
    Object.keys(depts).forEach(function(d){ if(depts[d] > topDeptCount){ topDept = d; topDeptCount = depts[d]; } });
    var hrDecided = hrApproved + hrRejected;
    var hrApprovalRate = hrDecided > 0 ? (hrApproved/hrDecided*100) : null;

    var narrativeExtra = [];
    narrativeExtra.push('This equates to a net profit margin of ' + profitMargin.toFixed(1) + '% and an average year-to-date gross payroll cost of ' + vuvFmt(avgPayrollPerEmployee) + ' per active employee.');
    if(cashRunwayMonths !== null) {
        var runwayStr = cashRunwayMonths.toFixed(1);
        narrativeExtra.push('Based on the average monthly operating expense of ' + vuvFmt(avgMonthlyExpense) + ' over the trailing six months, current cash on hand would cover approximately ' + runwayStr + (runwayStr === '1.0' ? ' month' : ' months') + ' of operations at that pace.');
    }
    if(topExpenseCat) {
        narrativeExtra.push('The largest single expense category was ' + topExpenseCat + ' at ' + vuvFmt(topExpenseAmt) + ', representing ' + topExpenseShare.toFixed(1) + '% of total recorded expenses.');
    }
    if(topDept && totalStaff > 0) {
        narrativeExtra.push('Staff headcount is concentrated in the ' + topDept + ' department, which accounts for ' + topDeptCount + ' of ' + totalStaff + ' employees.');
    }
    if(hrApprovalRate !== null) {
        narrativeExtra.push('Of the HR leave and advance requests decided so far this year, ' + hrApprovalRate.toFixed(0) + '% have been approved.');
    }

    el.innerHTML = `
        <p class="section-title"><i class="ti ti-chart-pie" style="color:var(--gold)"></i> <span>Executive Summary</span></p>
        <div id="exec-report-printable">
        <div class="rpt-page">
            ${rptHeader('Executive Summary Report', companyName)}
            ${rptMeta('Year to Date', rptToday(), 'Live')}
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">1.</span> Executive Summary</p>
                <table class="rpt-summary-table">
                    <tr><td>Company Revenue (YTD)</td><td>${vuvFmt(rev)}</td></tr>
                    <tr><td>Cash on Hand</td><td>${vuvFmt(cash)}</td></tr>
                    <tr><td>Net Profit (YTD)</td><td>${vuvFmt(netProfit)}</td></tr>
                    <tr><td>Total Expenses (YTD)</td><td>${vuvFmt(expenses)}</td></tr>
                    <tr><td>Total Payroll Gross (YTD)</td><td>${vuvFmt(ytdPayroll)}</td></tr>
                    <tr><td>VNPF Contributions (YTD)</td><td>${vuvFmt(ytdVNPF)}</td></tr>
                    <tr><td>Employees (Active / Total)</td><td>${activeStaff} / ${totalStaff}</td></tr>
                    <tr style="background:#f8f9fb"><td>Pending HR Requests</td><td>${pendingHR}</td></tr>
                </table>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">2.</span> Revenue vs Expense (6 Months)</p>
                <div style="position:relative;height:260px;width:100%"><canvas id="execChart1"></canvas></div>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">3.</span> Top Expense Categories</p>
                <div style="position:relative;height:260px;width:100%"><canvas id="execChart2"></canvas></div>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">4.</span> Staff by Department</p>
                <div style="position:relative;height:230px;width:100%"><canvas id="execChart3"></canvas></div>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">5.</span> HR Requests by Status</p>
                <div style="position:relative;height:230px;width:100%"><canvas id="execChart4"></canvas></div>
            </div>
            <div class="rpt-section">
                <p class="rpt-section-title"><span class="rpt-section-num">6.</span> Narrative Overview</p>
                <p style="font-size:13px;color:#444;line-height:1.8">This dashboard presents an aggregated, real-time view of WokManeja's operations. The company currently operates with ${activeStaff} active staff members (${totalStaff} total), having processed a total year-to-date gross payroll of ${vuvFmt(ytdPayroll)} and ${vuvFmt(ytdVNPF)} in VNPF contributions. Total cash on hand stands at ${vuvFmt(cash)} against recognized year-to-date revenue of ${vuvFmt(rev)} and expenses of ${vuvFmt(expenses)}, for a net profit of ${vuvFmt(netProfit)}. There ${pendingHR===1?'is':'are'} currently ${pendingHR} pending HR request${pendingHR===1?'':'s'} awaiting manager approval, out of ${hrApproved} approved and ${hrRejected} rejected to date. ${narrativeExtra.join(' ')}</p>
            </div>
            ${rptFooter()}
        </div>
        </div>
        ${reportActionBar('exec-report-printable', 'Executive-Summary.pdf', 'Executive Summary Report')}
    `;

    setTimeout(() => {
        if (typeof createChart !== 'function') return;
        createChart('execChart1', 'bar', {
            labels: trendLabels,
            datasets: [
                { label: 'Income', data: trendIncome, backgroundColor: '#10b981', borderRadius: 4 },
                { label: 'Expense', data: trendExpense, backgroundColor: '#ef4444', borderRadius: 4 }
            ]
        });
        createChart('execChart2', 'bar', {
            labels: expSorted.length ? expSorted : ['No Expenses'],
            datasets: [{
                label: 'Expenses',
                data: expSorted.length ? expSorted.map(k=>expenseBreakdown[k]) : [1],
                backgroundColor: PALETTE
            }]
        }, { indexAxis: 'y', plugins: { legend: { display: false } } });
        createChart('execChart3', 'bar', {
            labels: Object.keys(depts),
            datasets: [{ label: 'Staff', data: Object.values(depts), backgroundColor: PALETTE, borderRadius: 6 }]
        }, { plugins: { legend: { display: false } } });
        createChart('execChart4', 'bar', {
            labels: ['Approved', 'Pending', 'Rejected'],
            datasets: [{ label: 'Requests', data: [hrApproved, pendingHR, hrRejected], backgroundColor: ['#10b981','#f59e0b','#ef4444'], borderRadius: 6 }]
        }, { plugins: { legend: { display: false } } });
    }, 120);
}

// Reuses the .rpt-* classes already defined globally in index.html's <style> block.
function rptHeader(title, companyName) {
    return '<div class="rpt-header"><div class="rpt-logo-row">'+
        '<img src="logo.png" style="width:40px;height:40px;border-radius:10px">'+
        '<div><p class="rpt-title">'+title+'</p><p class="rpt-subtitle">'+companyName+'</p></div>'+
        '</div></div>';
}
function rptMeta(period, generatedOn, status) {
    return '<div class="rpt-meta">'+
        '<div class="rpt-meta-item"><p>Period</p><span>'+period+'</span></div>'+
        '<div class="rpt-meta-item"><p>Generated On</p><span>'+generatedOn+'</span></div>'+
        '<div class="rpt-meta-item"><p>Status</p><span style="color:#10b981">'+status+'</span></div>'+
        '</div>';
}
function rptFooter() {
    return '<div class="rpt-footer"><p>Generated by WokManeja</p><p>Page 1 of 1</p></div>';
}
function rptToday() {
    return new Date().toLocaleDateString('en-GB', { day: '2-digit', month: 'long', year: 'numeric' });
}
async function getCompanyName() {
    try {
        var res = await fetch('/api/settings');
        var list = await res.json();
        var c = list.find(function(s){ return s._id === 'company'; }) || {};
        return c.name || 'WokManeja';
    } catch(e) { return 'WokManeja'; }
}


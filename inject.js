const fs = require('fs');
const cheerio = require('cheerio');

const html = fs.readFileSync('./public/index.html', 'utf8');
const $ = cheerio.load(html, { decodeEntities: false });

const elementsToTranslate = ['label', 'button', 'th', 'td', 'p', 'span', 'h1', 'h2', 'h3', 'h4', 'h5', 'h6', 'div', 'h3'];

$('*').each((i, el) => {
  const tagName = el.tagName.toLowerCase();
  
  if (['script', 'style', 'svg', 'path', 'i'].includes(tagName)) return;
  
  const hasI18n = $(el).attr('data-i18n');
  
  // Check placeholders
  const placeholder = $(el).attr('placeholder');
  if (placeholder && !$(el).attr('data-i18n-placeholder')) {
    const pTrim = placeholder.trim();
    if (translations[pTrim]) {
      $(el).attr('data-i18n-placeholder', translations[pTrim].key);
    }
  }

  // Check text content
  if (!hasI18n && elementsToTranslate.includes(tagName)) {
    const textNodes = $(el).contents().filter(function() {
      return this.nodeType === 3 && this.nodeValue.trim().length > 0;
    });
    
    if(textNodes.length === 1) { // Only attempt if it's a simple text element to avoid messing up nested HTML
      const text = textNodes[0].nodeValue.trim();
      if (translations[text]) {
        // If it's a bare text node with siblings, we should wrap it, but it's simpler to set data-i18n on the parent if it contains ONLY this text.
        // Wait, if it has other children like <i>, we can wrap the text node in a span or just replace the text node with <span data-i18n="...">...</span>
        
        const span = $('<span data-i18n="'+translations[text].key+'"></span>').text(textNodes[0].nodeValue);
        $(textNodes[0]).replaceWith(span);
      }
    } else {
        textNodes.each((i, textNode) => {
            const text = textNode.nodeValue.trim();
            if (translations[text]) {
                const span = $('<span data-i18n="'+translations[text].key+'"></span>').text(textNodes[i].nodeValue);
                $(textNodes[i]).replaceWith(span);
            }
        });
    }
  }
});

// Update changeLang script logic
let outHTML = $.html();
outHTML = outHTML.replace(
  'document.querySelectorAll(\'[data-i18n]\').forEach(function(el){',
  'document.querySelectorAll(\'[data-i18n]\').forEach(function(el){\n    var key=el.getAttribute(\'data-i18n\');\n    if(I18N_DICT[key]&&I18N_DICT[key][lang]){el.innerHTML=I18N_DICT[key][lang];}\n  });\n  document.querySelectorAll(\'[data-i18n-placeholder]\').forEach(function(el){\n    var key=el.getAttribute(\'data-i18n-placeholder\');\n    if(I18N_DICT[key]&&I18N_DICT[key][lang]){el.setAttribute(\'placeholder\', I18N_DICT[key][lang]);}\n  });\n  // old loop block ignored:'
);

// Inject I18N_DICT additions
let dictStr = "";
for(let k in translations){
  let v = translations[k];
  dictStr += `  "${v.key}": { en: ${JSON.stringify(k)}, fr: ${JSON.stringify(v.fr)}, zh: ${JSON.stringify(v.zh)} },\n`;
}

outHTML = outHTML.replace('var I18N_DICT = {', 'var I18N_DICT = {\n' + dictStr);

